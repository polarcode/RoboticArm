function [rotm] = getHomogenRotMatrix (K0, K1)

K0 = tl(-K0(1,1), -K0(2,1), -K0(3,1))*K0;
K1 = tl(-K1(1,1), -K1(2,1), -K1(3,1))*K1;

K0 = K0(:,2:4);
K1 = K1(:,2:4);

A = [K0(1,1:3)',K0(2,1:3)',K0(3,1:3)'];

b = [K1(1,1);K1(1,2);K1(1,3)];
c = [K1(2,1);K1(2,2);K1(2,3)];
d = [K1(3,1);K1(3,2);K1(3,3)];


rotm = [(A\b)';(A\c)';(A\d)'];
rotm = [rotm ; [0 0 0]];
rotm = [rotm, [0 0 0 1]'];


end
